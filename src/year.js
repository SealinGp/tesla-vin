module.exports = letter => {
  switch (letter) {
    case 'C':
      return 2012
    case 'D':
      return 2013
    case 'E':
      return 2014
    case 'F':
      return 2015
    case 'G':
      return 2016
    case 'H':
      return 2017
    case 'J':
      return 2018
    case 'K':
      return 2019
    case 'L':
      return 2020
    case 'M':
      return 2021
    case 'N':
      return 2022
    case 'P':
      return 2023
    case 'R':
      return 2024
    default:
      return null
  }
}
